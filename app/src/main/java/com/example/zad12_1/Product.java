package com.example.zad12_1;

public class Product {
        private String firstProdukt;
        private float cena;

        public Product(String firstProdukt, float cena) {
            this.firstProdukt = firstProdukt;
            this.cena = cena;
        }

        public String getFirstProdukt() {
            return firstProdukt;
        }

        public float getCena() {
            return cena;
        }
    }

